const endSaal = 2090;

List<int> saal(int year) {
  if (year < startSaal && year > endSaal) {
    throw RangeError.range(year, startSaal, endSaal, 'year');
  }
  return _saal[year - startSaal];
}

int totalSaals() {
  return _saal.length;
}
